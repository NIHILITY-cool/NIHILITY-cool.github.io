function escapeHtml(str) {
  return String(str ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function resetState() {
  submitted = false;
  userAnswers = {};
  resultPanel.classList.add("hidden");
}

function setMode(nextMode) {
  if (mode === nextMode) return;
  mode = nextMode;
  memorizeBtn.classList.toggle("active", mode === "memorize");
  testBtn.classList.toggle("active", mode === "test");
  resetState();
  if (mode === "test") pickRandomArticle(true);
  render();
}

function pickRandomArticle(forceDifferent = true) {
  if (!DATA.length) return;
  let next = currentIndex;
  if (DATA.length > 1) {
    while (forceDifferent && next === currentIndex) {
      next = Math.floor(Math.random() * DATA.length);
    }
  } else {
    next = 0;
  }
  currentIndex = next;
  testHistory = testHistory.slice(0, historyPos + 1);
  testHistory.push(currentIndex);
  historyPos = testHistory.length - 1;
  resetState();
}

function moveArticle(delta) {
  if (mode === "memorize") {
    currentIndex = (currentIndex + delta + DATA.length) % DATA.length;
    resetState();
  } else {
    if (delta < 0 && historyPos > 0) {
      historyPos -= 1;
      currentIndex = testHistory[historyPos];
      resetState();
    } else {
      pickRandomArticle(true);
    }
  }
  render();
}

function renderHeader() {
  const a = article();
  articleKicker.textContent = `${a.label}`;
  articleTitle.textContent = a.title;
  articleRange.textContent = a.range;
  articleProgress.textContent = mode === "memorize"
    ? `文章 ${currentIndex + 1} / ${DATA.length}`
    : `随机自测 · 文章 ${currentIndex + 1} / ${DATA.length}`;
  articleCount.textContent = `${a.questions.length} 题`;
  progressFill.style.width = `${((currentIndex + 1) / DATA.length) * 100}%`;
  testTools.classList.toggle("show", mode === "test");
}

function renderQuestions() {
  const a = article();
  questionArea.innerHTML = a.questions.map(q => renderQuestion(q)).join("");

  if (mode === "test" && !submitted) {
    document.querySelectorAll(".option-card.clickable").forEach(btn => {
      btn.addEventListener("click", () => {
        const qn = btn.dataset.q;
        userAnswers[qn] = btn.dataset.letter;
        render();
      });
    });
  }
}

function renderQuestion(q) {
  const selected = userAnswers[q.number];
  const showAnswer = mode === "memorize" || submitted;
  const pill = showAnswer ? `<div class="correct-pill">正确答案 ${q.correct}</div>` : "";
  const translation = q.translation ? `<p class="q-translation">${escapeHtml(q.translation)}</p>` : "";
  const opts = q.options.map(opt => renderOption(q, opt, selected)).join("");
  const feedback = submitted ? renderFeedback(q, selected) : "";
  return `
    <article class="question-card">
      <div class="q-head">
        <div class="q-num">${q.number}</div>
        <div class="q-text-wrap">
          <p class="q-text">${escapeHtml(q.question || `第${q.number}题`)}</p>
          ${translation}
        </div>
        ${pill}
      </div>
      <div class="option-list">${opts}</div>
      ${feedback}
    </article>`;
}

function renderOption(q, opt, selected) {
  let cls = "option-card";
  if (mode === "test" && !submitted) cls += " clickable";
  if (mode === "memorize" && opt.letter === q.correct) cls += " correct";
  if (mode === "test" && !submitted && selected === opt.letter) cls += " selected";
  if (submitted) {
    if (opt.letter === q.correct) cls += " correct";
    else if (selected === opt.letter) cls += " wrong";
  }
  return `
    <button class="${cls}" data-q="${q.number}" data-letter="${opt.letter}" type="button">
      <span class="option-letter">${opt.letter}</span>
      <span class="option-text">${escapeHtml(opt.text)}</span>
    </button>`;
}

function renderFeedback(q, selected) {
  if (selected === q.correct) {
    return `<div class="feedback good">本题正确。</div>`;
  }
  const correct = q.options.find(o => o.letter === q.correct);
  return `<div class="feedback bad">你的选择：${selected || "未选"}；正确答案：${q.correct}. ${escapeHtml(correct?.text || "")}</div>`;
}

function renderActions() {
  const isTest = mode === "test";
  submitBtn.classList.toggle("hidden", !(isTest && !submitted));
  retryBtn.classList.toggle("hidden", !(isTest && submitted));
  againBtn.classList.toggle("hidden", !(isTest && submitted));
}

function renderResult() {
  if (mode !== "test" || !submitted) {
    resultPanel.classList.add("hidden");
    return;
  }
  const a = article();
  const total = a.questions.length;
  const right = a.questions.filter(q => userAnswers[q.number] === q.correct).length;
  const wrong = a.questions
    .filter(q => userAnswers[q.number] !== q.correct)
    .map(q => `第${q.number}题（你选：${userAnswers[q.number] || "未选"}，正确：${q.correct}）`);
  resultPanel.innerHTML = `
    <h3 class="result-title">本篇得分：<strong>${right} / ${total}</strong>，正确率 <strong>${Math.round(right / total * 100)}%</strong></h3>
    <p class="result-detail">${wrong.length ? "错题：" + wrong.join("；") : "全部正确，可以换下一篇随机自测。"}</p>`;
  resultPanel.classList.remove("hidden");
}

function submitCurrent() {
  submitted = true;
  render();
  resultPanel.scrollIntoView({ behavior: "smooth", block: "nearest" });
}

function retryCurrent() { resetState(); render(); }
function againRandom() { pickRandomArticle(true); render(); }

function nextArticleSequential() {
  if (!DATA.length) return;
  const next = (currentIndex + 1) % DATA.length;
  currentIndex = next;
  testHistory = testHistory.slice(0, historyPos + 1);
  testHistory.push(currentIndex);
  historyPos = testHistory.length - 1;
  resetState();
  render();
}
function render() {
  if (!DATA.length) return;
  renderHeader();
  renderQuestions();
  renderResult();
  renderActions();
}

memorizeBtn.addEventListener("click", () => setMode("memorize"));
testBtn.addEventListener("click", () => setMode("test"));
prevBtn.addEventListener("click", () => moveArticle(-1));
nextBtn.addEventListener("click", () => moveArticle(1));
submitBtn.addEventListener("click", submitCurrent);
retryBtn.addEventListener("click", retryCurrent);
againBtn.addEventListener("click", againRandom);
randomBtn.addEventListener("click", againRandom);
nextArticleBtn.addEventListener("click", nextArticleSequential);
document.addEventListener("keydown", (e) => {
  if (e.key === "ArrowLeft") moveArticle(-1);
  if (e.key === "ArrowRight") moveArticle(1);
});

render();
